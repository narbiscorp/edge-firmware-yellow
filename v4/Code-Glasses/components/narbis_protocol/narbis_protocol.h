/* =============================================================
 * BLE UUIDs (generated by protocol/generate_uuids.py)
 *
 * The _STR form is used by ble_gatts setup paths that take strings.
 * The _BYTES form is the little-endian byte order required by ESP-IDF's
 * esp_bt_uuid_t / ble_uuid128_t fields.
 *
 * Standard SIG services (used as-is, no custom UUIDs):
 *   0x180D  Heart Rate Service
 *   0x180F  Battery Service
 *   0x180A  Device Information Service
 * ============================================================= */

/* NOTE on byte order: the _BYTES form must be pure little-endian, i.e.
 * `value[0]` is the LSB of the UUID. NimBLE's `ble_uuid128_t.value` and
 * ESP-IDF's `esp_bt_uuid_t.uuid.uuid128` both use this layout, and it is
 * what gets emitted on the wire as-is. Earlier revisions of this header
 * mistakenly used Microsoft GUID layout (first 3 fields little-endian,
 * last 2 fields big-endian). That caused the firmware to advertise and
 * register service UUIDs that did NOT match the strings, which in turn
 * caused dashboard's getPrimaryService() to fail with NotFoundError. To
 * sanity-check a new entry: take the _STR form, strip dashes, pair into
 * bytes (big-endian display order), then reverse the entire 16-byte
 * sequence to get _BYTES. */

