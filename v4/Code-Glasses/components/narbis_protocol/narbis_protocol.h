/* CRC-16-CCITT-FALSE: poly 0x1021, init 0xFFFF, no reflect, no xor-out. */
uint16_t narbis_crc16_ccitt_false(const uint8_t *data, size_t len);

/* Returns the wire payload size for the given message type and packet,
 * or 0 if msg_type is unknown. RAW_PPG depends on n_samples. */
size_t narbis_payload_size(const narbis_packet_t *pkt);

/* Serialize pkt into buf. *out_len is set to the total bytes written
 * (header + payload + crc). Returns 0 on success, negative on error
 * (buf too small / unknown msg_type / payload too large). */
int narbis_packet_serialize(uint8_t *buf, size_t buf_len,
                            const narbis_packet_t *pkt, size_t *out_len);

/* Deserialize buf into pkt. Verifies CRC, protocol_version, and that
 * payload_len matches msg_type. Returns 0 on success, negative on error. */
int narbis_packet_deserialize(const uint8_t *buf, size_t buf_len,
                              narbis_packet_t *pkt);

/* Serialize / deserialize the runtime config. The wire form is just the
 * packed struct bytes followed by a CRC-16. */
int narbis_config_serialize(uint8_t *buf, size_t buf_len,
                            const narbis_runtime_config_t *cfg, size_t *out_len);
int narbis_config_deserialize(const uint8_t *buf, size_t buf_len,
                              narbis_runtime_config_t *cfg);

